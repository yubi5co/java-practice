
public class stringapp {

	public static void main(String[] args) {
		
		System.out.println("hello");                 // string 문자열
		//System.out.println('hello');               // 자바에서는 '' character 을 나타낸다.
		System.out.println('h');                     // character 문자 
		
		// new line
		System.out.println("hello \nworld");         // \n: 줄바꿈
		// escape
		System.out.println("hello \"world\"");       // \"\": hello "world" 생성     

	}

}
