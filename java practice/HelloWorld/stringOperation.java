
public class stringOperation {

	public static void main(String[] args) {
		
		
		System.out.println("hello world".length());        // .length() 글자수 
		System.out.println("packing".toUpperCase());       // .toUpperCase() 소문자를 대문자로 변환
		System.out.println("hello, [name].... bye.".replace("[name]", "egoing")); // .replace 교체
		

	}

}
