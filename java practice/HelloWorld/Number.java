
public class Number {

	public static void main(String[] args) {
		
		// operator (연산자)
		System.out.println(6 + 2); 
		System.out.println(6 - 2);
		System.out.println(6 * 2);
		System.out.println(6 / 2);
		
		System.out.println(Math.PI);              // 3.1415..... 원주율
		System.out.println(Math.floor(Math.PI));  //3.0 반올림
		System.out.println(Math.ceil(Math.PI));   //4.0 반올림
	}

}
